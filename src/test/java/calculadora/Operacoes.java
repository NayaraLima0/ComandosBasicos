package calculadora;

public class Operacoes {

	public void soma(int x1, int x2) {
		int resultado = x1 + x2;
		System.out.println("o resultado da soma é " + resultado);
		
	}
	
	public void subtracao (int x1, int x2) {
		int resultadoSub = x1 - x2;
		System.out.println("o resultado da subtração é " + resultadoSub);
	}
 public void divisao (int x1, int x2) {
	 int resultadoDiv = x1 - x2;
	 System.out.println("o resultado da divisão é " + resultadoDiv);
 }
 public void multiplicacao (int x1, int x2) {
	 int resultadoMult = x1 * x2;
	 System.out.println("o resultado da multiplicação é " + resultadoMult);
 }
 
 
}
